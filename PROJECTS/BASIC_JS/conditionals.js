// If statement
console.log("\n---------EXAMPLE 1-----------");
if (true) {
    console.log("Entered if statement");
}

// If else statement
console.log("\n---------EXAMPLE 2-----------");
if (false) {
    console.log("Entered if statement");
} else {
    console.log("Entered the else statement");
}

// If else-if statement
console.log("\n---------EXAMPLE 3-----------");
if (false) {
    console.log("Entered if statement");
} else if (false) {
    console.log("Entered the first else-if statement");
} else if (true) {
    console.log("Entered the second else-if statement");
} else {
    console.log("Entered the else statement");
}
